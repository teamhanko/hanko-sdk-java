package io.hanko.sdk.http;

/**
 * Enumeration of HTTP methods.
 */
public enum HttpMethod {
    /**
     * Represents a HTTP GET method.
     */
    GET,
    /**
     * Represents a HTTP POST method.
     */
    POST,
    /**
     * Represents a HTTP PUT method.
     */
    PUT,
    /**
     * Represents a HTTP DELETE method.
     */
    DELETE;
}