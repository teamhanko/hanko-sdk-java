package io.hanko.sdk.models;

public class RelyingParty {
    private String id;
    private String name;
    private String appId;
    private String icon;

    public String getId() {
        return id;
    }

    public String getName() {
        return name;
    }

    public String getAppId() {
        return appId;
    }

    public String getIcon() {
        return icon;
    }
}
